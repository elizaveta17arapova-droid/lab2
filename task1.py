# 1. поиск самого длинного слова в строке через пробел
# просим пользователя ввести строку
text = input("Введите строку (слова через пробел): ")
# разбиваем строку на список слов по пробелу
words = text.split(" ")
# переменная для хранения самого длинного слова
longest = ""
# перебираем каждое слово из списка
for w in words:
    # убираем лишние пробелы вокруг слова
    word = w.strip()
    # если длина слова больше, чем у текущего самого длинного
    if len(word) > len(longest):
        longest = word
print("Самое длинное слово:", longest)
